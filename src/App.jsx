function getRankProgress(xp) {
  const { current, next } = getRankByXp(xp);
  if (!next) return 100;
  return Math.max(0, Math.min(100, Math.round(((xp - current.xp) / (next.xp - current.xp)) * 100)));
}

function makeQuizOptions(card, seed) {
  const options = [card];
  const pool = WORDS.filter((word) => word.id !== card.id);
  let cursor = Math.abs(seed * 7 + card.id * 3);
  while (options.length < 4 && pool.length > 0) {
    const candidate = pool[cursor % pool.length];
    if (!options.some((word) => word.id === candidate.id)) options.push(candidate);
    cursor += 11;
  }
  return options.sort((a, b) => ((a.id * 13 + seed) % 17) - ((b.id * 13 + seed) % 17));
}

function dayNumber() {
  return Math.floor(Date.now() / 86400000);
}

function getReviewDelay(mode, oldBox = 0) {
  if (mode === "wrong") return { box: 0, due: dayNumber() };
  if (mode === "hard") return { box: Math.max(0, oldBox), due: dayNumber() + 1 };
  const nextBox = Math.min(5, oldBox + 1);
  const delays = [1, 2, 4, 7, 14, 30];
  return { box: nextBox, due: dayNumber() + delays[nextBox] };
}

function updateReviewSchedule(schedule, cardId, mode) {
  const next = { ...(schedule || {}) };
  const oldBox = Number(next[cardId]?.box || 0);
  next[cardId] = getReviewDelay(mode, oldBox);
  return next;
}

function isDueWord(word, schedule) {
  const item = schedule?.[word.id];
  if (!item) return true;
  return Number(item.due || 0) <= dayNumber();
}

function buildStudyDeck(words, state) {
  const deck = [];
  words.forEach((word) => {
    const review = state.reviewSchedule?.[word.id];
    const isDue = isDueWord(word, state.reviewSchedule);
    const wrongWeight = Math.min(5, Number(state.wrongCounts?.[word.id] || 0));
    const hardWeight = state.hardList.includes(word.id) ? 2 : 0;
    const dueWeight = isDue ? 3 : 0;
    const masteredPenalty = state.mastered.includes(word.id) && !isDue ? 2 : 0;
    const futurePenalty = review && !isDue ? 1 : 0;
    const repeat = Math.max(1, 1 + wrongWeight + hardWeight + dueWeight - masteredPenalty - futurePenalty);
    for (let i = 0; i < repeat; i += 1) deck.push(word);
  });
  return deck;
}

function updateWrongCounts(oldCounts, cardId, mode) {
  const next = { ...(oldCounts || {}) };
  const current = Number(next[cardId] || 0);
  if (mode === "wrong") next[cardId] = Math.min(8, current + 2);
  if (mode === "hard") next[cardId] = Math.min(8, current + 1);
  if (mode === "known") {
    if (current <= 1) delete next[cardId];
    else next[cardId] = current - 1;
  }
  return next;
}

